using ReelWords.Utilities;

namespace ReelWords.Game;

public class Tile
{
    //------------------------------------------------------------------------------------------------------------------
    // Properties
    //------------------------------------------------------------------------------------------------------------------
    public char Letter { get; }
    public int Score { get; set; }
    
    //------------------------------------------------------------------------------------------------------------------
    // Methods
    //------------------------------------------------------------------------------------------------------------------
    public Tile(char letter)
    {
        Letter = letter;
    }
    
    //------------------------------------------------------------------------------------------------------------------
    public override string ToString()
    {
        return Letter.ToString().ToUpper() + Score.ToSubscript();
    }
}
